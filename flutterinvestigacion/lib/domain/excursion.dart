class Excursion {
  final int id;
  final String descripcion;
  final DateTime fecha;
  final double precio;
  final int estado;
  final String lugar;
  final int cantidad;


  Excursion({
    required this.id,
    required this.descripcion,
    required this.fecha,
    required this.precio,
    required this.estado,
    required this.lugar,
    required this.cantidad,
  });
}